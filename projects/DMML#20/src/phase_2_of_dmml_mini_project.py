class AdvancedAdversarialURLGenerator:
    """
    Advanced URL transformation techniques to test adversarial robustness.
    Based on known phishing and malware evasion tactics.
    """

    # EVASION TECHNIQUE 1: ENCODING OBFUSCATION
    @staticmethod
    def double_url_encoding(url):
        """Apply double URL encoding to bypass filters."""
        return quote(quote(url))

    @staticmethod
    def unicode_encoding(url):
        """Convert characters to unicode escape sequences."""
        if '://' in url:
            proto, rest = url.split('://', 1)
            # Apply unicode normalization
            encoded = ''.join(f'%u{ord(c):04x}' if ord(c) > 127 else c for c in rest)
            return f"{proto}://{encoded}"
        return url

    @staticmethod
    def hex_encoding(url):
        """Encode domain as hexadecimal."""
        if '://' in url:
            proto, rest = url.split('://', 1)
            if '/' in rest:
                domain, path = rest.split('/', 1)
                hex_domain = '0x' + ''.join(f'{ord(c):02x}' for c in domain.split('.')[0])
                return f"{proto}://{hex_domain}.{'.'.join(domain.split('.')[1:])}/{path}"
        return url

    # EVASION TECHNIQUE 2: IP-BASED OBFUSCATION
    @staticmethod
    def convert_to_ip_decimal(url):
        """Convert domain IP to decimal notation (if IP present)."""
        ip_pattern = r'\\b(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\b'

        match = re.search(ip_pattern, url)
        if match:
            ip = match.group()
            octets = [int(x) for x in ip.split('.')]
            decimal_ip = octets[0] * 256**3 + octets[1] * 256**2 + octets[2] * 256 + octets[3]
            return url.replace(ip, str(decimal_ip))
        return url

    @staticmethod
    def convert_to_ip_octal(url):
        """Convert domain IP to octal notation."""
        ip_pattern = r'\\b(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\b'

        match = re.search(ip_pattern, url)
        if match:
            ip = match.group()
            octets = [int(x) for x in ip.split('.')]
            octal_ip = '.'.join(oct(o)[2:] for o in octets)
            return url.replace(ip, octal_ip)
        return url

    # EVASION TECHNIQUE 3: DOMAIN MANIPULATION
    @staticmethod
    def add_null_byte(url):
        """Inject null bytes to confuse parsers."""
        if '://' in url:
            proto, rest = url.split('://', 1)
            # Insert null byte after domain
            if '/' in rest:
                domain, path = rest.split('/', 1)
                return f"{proto}://{domain}%00.good-domain.com/{path}"
        return url

    @staticmethod
    def add_user_info(url):
        """Add fake user:password to domain."""
        if '://' in url:
            proto, rest = url.split('://', 1)
            return f"{proto}://user:pass@{rest}"
        return url

    @staticmethod
    def use_shortener_like_pattern(url):
        """Mimic URL shortener patterns."""
        random_id = ''.join(random.choices(string.ascii_letters + string.digits, k=6))
        return f"https://tinyurl.com/{random_id}"

    # EVASION TECHNIQUE 4: QUERY PARAMETER OBFUSCATION
    @staticmethod
    def add_chaff_parameters(url):
        """Add irrelevant parameters to obscure real ones."""
        params = [
            'utm_source=google',
            'utm_medium=search',
            'utm_campaign=spring',
            'fbclid=1234567890',
            'gclid=0.0.0.0',
            'msclkid=abcdef123456'
        ]
        separator = '&' if '?' in url else '?'
        return url + separator + '&'.join(random.sample(params, k=2))

    @staticmethod
    def fragment_obfuscation(url):
        """Add fragment to hide suspicious parts from logs."""
        return f"{url}#legitimate-looking"

    # EVASION TECHNIQUE 5: HOMOGRAPH ATTACKS
    @staticmethod
    def homograph_attack(url):
        """Replace characters with lookalikes (i→1, o→0, etc)."""
        replacements = {
            'i': '1',
            'o': '0',
            'a': '@',
            's': '5',
            'e': '3',
            'b': '8'
        }

        if '://' in url:
            proto, rest = url.split('://', 1)
            if '/' in rest:
                domain, path = rest.split('/', 1)
                # Replace in domain
                domain = ''.join(replacements.get(c, c) for c in domain)
                return f"{proto}://{domain}/{path}"
        return url

    @staticmethod
    def internationalized_domain(url):
        """Use internationalized domain names (IDN) for spoofing."""
        if '://' in url:
            proto, rest = url.split('://', 1)
            if '/' in rest:
                domain, path = rest.split('/', 1)
                # Replace with punycode-like appearance
                return f"{proto}://xn--{domain.replace('.', '--')}.{path.split('/')[0] if path else 'com'}"
        return url

    # EVASION TECHNIQUE 6: SEMANTIC ATTACKS
    @staticmethod
    def case_manipulation(url):
        """Use mixed case to evade case-sensitive filters."""
        result = ""
        for char in url:
            if char.isalpha():
                result += char.upper() if random.random() > 0.5 else char.lower()
            else:
                result += char
        return result

    @staticmethod
    def whitespace_injection(url):
        """Inject whitespace that some parsers ignore."""
        return url.replace('//', '/ /')

    # COMPREHENSIVE ATTACK BATCH
    @staticmethod
    def generate_advanced_attacks(original_url, intensity='medium'):
        """
        Generate comprehensive attack variants.

        Args:
            original_url: Original URL
            intensity: 'low' (3 attacks), 'medium' (7), 'high' (all)

        Returns:
            List of (adversarial_url, attack_method, difficulty) tuples
        """
        all_attacks = [
            (AdvancedAdversarialURLGenerator.double_url_encoding, 'double_url_encoding', 'low'),
            (AdvancedAdversarialURLGenerator.hex_encoding, 'hex_encoding', 'medium'),
            (AdvancedAdversarialURLGenerator.add_null_byte, 'null_byte_injection', 'high'),
            (AdvancedAdversarialURLGenerator.add_user_info, 'user_info_obfuscation', 'medium'),
            (AdvancedAdversarialURLGenerator.add_chaff_parameters, 'chaff_parameters', 'low'),
            (AdvancedAdversarialURLGenerator.fragment_obfuscation, 'fragment_obfuscation', 'low'),
            (AdvancedAdversarialURLGenerator.homograph_attack, 'homograph_attack', 'medium'),
            (AdvancedAdversarialURLGenerator.case_manipulation, 'case_manipulation', 'low'),
            (AdvancedAdversarialURLGenerator.add_chaff_parameters, 'parameter_pollution', 'low'),
        ]

        if intensity == 'low':
            attacks_to_use = all_attacks[:3]
        elif intensity == 'medium':
            attacks_to_use = all_attacks[:7]
        else:  # high
            attacks_to_use = all_attacks

        results = [(original_url, 'original', 'baseline')]

        for attack_func, attack_name, difficulty in attacks_to_use:
            try:
                attacked_url = attack_func(original_url)
                results.append((attacked_url, attack_name, difficulty))
            except:
                # Catching exceptions during attack generation to continue process
                pass

        return results


