class AdversarialMetrics:
    """
    Compute advanced adversarial robustness metrics.
    """

    @staticmethod
    def compute_attack_success_rate(y_true, y_pred_original, y_pred_adversarial):
        """
        Compute attack success rate.

        Returns:
            Percentage of adversarial examples that successfully evade detection
        """
        successful_attacks = 0
        for i in range(len(y_true)):
            # Attack successful if: model correctly classifies original but misclassifies adversarial
            if y_pred_original[i] == y_true[i] and y_pred_adversarial[i] != y_true[i]:
                successful_attacks += 1

        return successful_attacks / len(y_true)

    @staticmethod
    def compute_robustness_score(results_by_difficulty):
        """
        Compute overall robustness score based on attack difficulty.

        Args:
            results_by_difficulty: Dict with keys 'low', 'medium', 'high'
                                  containing success rates

        Returns:
            Weighted robustness score (0-1, higher is better)
        """
        weights = {'low': 0.2, 'medium': 0.3, 'high': 0.5}
        score = 1.0  # Start at perfect

        for difficulty, weight in weights.items():
            if difficulty in results_by_difficulty:
                success_rate = results_by_difficulty[difficulty]
                score -= weight * success_rate

        return max(0, score)

    @staticmethod
    def compute_certified_robustness_radius(X_features, model_pred, epsilon=0.1):
        """
        Estimate certified robustness radius (feature space perturbation tolerance).

        Returns:
            Minimum distance to decision boundary for each sample
        """
