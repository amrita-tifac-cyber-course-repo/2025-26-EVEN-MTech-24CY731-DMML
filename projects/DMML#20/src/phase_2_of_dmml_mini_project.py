# Ensure 'domain' column is iterated safely in case of empty strings
for d in final_df['domain'].dropna(): # Drop NaN domains before processing
    all_bigrams.extend(generate_bigrams(d))

bigram_counts = Counter(all_bigrams)
total_bigrams = sum(bigram_counts.values())

def bigram_score(domain):
    bigrams = generate_bigrams(domain)
    score = 0
    # Handle case where bigrams list is empty to prevent ZeroDivisionError
    if not bigrams or total_bigrams == 0: # Also check total_bigrams for 0
        return 0.0
    for bg in bigrams:
        freq = bigram_counts.get(bg, 0.5) / total_bigrams
        score += -math.log2(freq) if freq > 0 else 0 # Handle freq = 0 case
    return score / len(bigrams) if bigrams else 0

