class EnsembleAnalyzer:
    """
    Detailed analysis of ensemble model behavior and agreement patterns.
    """

    def __init__(self, detector):
        self.detector = detector

    def model_disagreement_analysis(self, X_features, y_true):
        """
        Analyze instances where models disagree.

        Returns:
            DataFrame with disagreement instances
        """
        predictions = {
            'xgb': None,
            'cat': None,
            'mlp': None,
            'ensemble': None
        }

        if self.detector.xgb_model:
            predictions['xgb'] = self.detector.xgb_model.predict(X_features)

        if self.detector.cat_model:
            predictions['cat'] = self.detector.cat_model.predict(X_features)

        if self.detector.mlp_model:
            # Assuming the MLP model uses scaled features
            X_scaled = self.detector.scaler.transform(X_features)
            predictions['mlp'] = (self.detector.mlp_model.predict(X_scaled, verbose=0).flatten() > 0.5).astype(int)

        ensemble_proba, ensemble_pred, _ = self.detector.predict(X_features)
        predictions['ensemble'] = ensemble_pred

        # Find disagreements
        disagreements = []
        for i in range(len(X_features)):
            preds = [predictions[k][i] for k in predictions if predictions[k] is not None]
            if len(set(preds)) > 1:  # Not all predictions agree
                disagreements.append({
                    'index': i,
                    'ensemble_pred': predictions['ensemble'][i],
                    'ensemble_proba': ensemble_proba[i],
                    'true_label': y_true[i],
                    'xgb': predictions['xgb'][i] if predictions['xgb'] is not None else None,
                    'cat': predictions['cat'][i] if predictions['cat'] is not None else None,
                    'mlp': predictions['mlp'][i] if predictions['mlp'] is not None else None,
                })

        return pd.DataFrame(disagreements)

